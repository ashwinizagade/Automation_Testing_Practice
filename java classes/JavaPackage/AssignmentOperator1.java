package JavaPackage;

public class AssignmentOperator1 {

	public static void main(String[] args) {
		
		 int a=10; //local variable
		 
		 System.out.println(a  +=  5); //15 do not pass space between + and =
		 
		 System.out.println(a  -=   5); // 10 do not pass space between + and =
		 
		 System.out.println(a  *=  2);//20 do not pass space between + and =
		 
		 System.out.println(a  /=   5); // 4 
		 
		 

	}

}
