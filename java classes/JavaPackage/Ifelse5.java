package JavaPackage;

public class Ifelse5 {

	public static void main(String[] args) {
		
		String s = "hello java";
		String y = "HELLO JAVA";
		
		if (s == y) {
			System.out.println("its correct");

		} else if (s != y) {
			System.out.println("its not correct");

		} else {
			System.out.println("Syntax Error");
		}
	}

}
