package JavaPackage;

public class IfElse3 {

	public static void main(String[] args) {

		int q = 300; // local variable
		int w = 200; // local variable
		if (q < w) {
			System.out.println("q is lesser");

		} else if (q == w) {
			System.out.println("q is equal to w");
		} else {
			System.out.println("");
		}
	}
}